    public static string Get(string key, string languageTag)
    {
        if (Map.TryGetValue(key, out var pair))
            return Pick(pair, languageTag);
        if (Vm.TryGetValue(key, out pair))
            return Pick(pair, languageTag);
        if (UiTranslationExtras.TryGet(key, out pair))
            return Pick(pair, languageTag);
        return key;
    }

    private static string Pick((string Fr, string Ar) pair, string languageTag) =>
        languageTag.StartsWith("ar", StringComparison.OrdinalIgnoreCase) ? pair.Ar : pair.Fr;
